i18n
  .use(initReactI18next)
  .init({
    resources,
    lng: 'en',
    fallbackLng: 'en',
    interpolation: { escapeValue: false },
  });

// Restore persisted language (if any) and expose a helper to change+persist
(async () => {
  try {
    const saved = await AsyncStorage.getItem('renax_lang');
    if (saved && saved !== i18n.language) {
      await i18n.changeLanguage(saved);
    }
  } catch (e) {
    // Non-fatal: continue with default
    // eslint-disable-next-line no-console
    console.warn('i18n: failed to load saved language', e);
  }
})();

export const setAppLanguage = async (code) => {
  try {
    await AsyncStorage.setItem('renax_lang', code);
    await i18n.changeLanguage(code);
  } catch (e) {
    // eslint-disable-next-line no-console
    console.warn('i18n: failed to persist language', e);
  }
};

export default i18n;
